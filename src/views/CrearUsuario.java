	//Código que permite mover la ventana por la pantalla según la posición de "x" y "y"	
	 private void headerMousePressed(java.awt.event.MouseEvent evt) {
	        xMouse = evt.getX();
	        yMouse = evt.getY();
	 }

	 private void headerMouseDragged(java.awt.event.MouseEvent evt) {
	        int x = evt.getXOnScreen();
	        int y = evt.getYOnScreen();
	        this.setLocation(x - xMouse, y - yMouse);
	 }
	 
	 private void guardar() {
		 var listUsuarios = this.usuarioController.listar();
		 String usrRegistrado = null;
		 String fechaNacimiento = null;
		 
		 for(Usuario usuarios : listUsuarios) {
				
		        if(usuario.getText().equals(usuarios.getUsuario())){
		        	usrRegistrado = usuarios.getUsuario();
		        }
		 }
			
		 if(usuario.getText().equals(usrRegistrado)){
				JOptionPane.showMessageDialog(this, "El usuario ya se encuentra registrado");
	            return;
	     }
		 
		 System.out.println(usrRegistrado);
		 System.out.println(usuario.getText());
		 
		 if (usuario.getText().isBlank() || txtNombre.getText().isBlank() || psw.getText().isBlank() 
				 || txtTelefono.getText().isBlank()) {
	            JOptionPane.showMessageDialog(this, "Todos los campos son requeridos para registrar al Usuario");
	            return;
	        }
		 
		 try {
			 String formato = txtFechaN.getDateFormatString();
			 java.util.Date fecha = txtFechaN.getDate();
			 SimpleDateFormat sdf = new SimpleDateFormat(formato);
			 fechaNacimiento = sdf.format(fecha);
		 } catch(NullPointerException e) {
		     JOptionPane.showMessageDialog(this, "Al menos selecciona una fecha", "Error!", JOptionPane.INFORMATION_MESSAGE);
		 }
		 
	     var usuarios = new Usuario(
	    		 usuario.getText(),
	    		 txtNombre.getText(), 
	    		 psw.getText(), 
	    		 fechaNacimiento,
	    		 txtNacionalidad.getSelectedItem().toString(),
	    		 txtTelefono.getText());
	        

			this.usuarioController.guardar(usuarios);

	        JOptionPane.showMessageDialog(this, "Registrado con éxito!");

	        Login login= new Login();
	        login.setVisible(true);
	        dispose();
		}
